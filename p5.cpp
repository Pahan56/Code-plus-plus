#include <iostream>
using namespace std;
int main()
{

    int a,b;
    cout<<"Enter 1st no:";
    cin>>a;
    cout<<"Enter 2nd no:";
    cin>>b;

    cout<<"Add" <<a+b <<endl;
    cout<<"Substract" <<a-b <<endl;
    cout<<"Division"  <<a/b <<endl;
    cout<<"Multiply " <<a*b <<endl;
    cout<<"a++" <<a++ <<endl;
    cout<<"a--" <<a-- <<endl;
    cout<<"a%b" <<a%b <<endl;
    return 0;


}
